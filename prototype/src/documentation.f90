!> \section Description Description
!>
!> This is a draft for a guide to the facilities offered by Selalib,
!> the Semi-Lagrangian Library. It serves as a record for module
!> descriptions, design decision, pending issues, etc., which are
!> relevant for the development and improvement of the library. The
!> main focus is the exposed API, thus the document also serves as a
!> working document for the architectural specifications. More specific
!> implementation details should be found in the comments within the
!> source code. We may decide later to extract those either with an
!> automatic system or in a developer's manual. At the time of writing
!> this version of the draft, the library contains only a few modules
!> in the lower levels and aims at having a minimalist structure. Of
!> those capabilities that could be deemed interesting enough by the
!> managers of GYSELA, the intent would be to incorporate them that
!> code. In this sense, this library prototype can also be a testbed
!> for ideas and suggestions that could eventually be absorbed into
!> production code. 
